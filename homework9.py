# რეკურსიის გამოყენებით დაწერეთ პითონის ფუნქცია, რომელიც მიიღებს პარამეტრად სტრიქონს და
# დააბრუნებს მის შებრუნებულ (revers) სტრიქონს ( მაგალითად input:Hello Output: olleH)

def rec(s):
    if len(s) == 0:
        return s
    else:
        return rec(s[1:]) + s[0]
    
print(rec(input("Enter something...")))


