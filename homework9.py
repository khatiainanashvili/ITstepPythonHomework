# დაწერეთ რეკურსიული ფუნქცია, დააბრუნებს ფიბონაჩის მიმდევრობას.
# ფიბონაჩის მიმდევრობის თანახმად ყოველი რიცხვი წინა ორი რიცხვის ჯამია
# 0, 1, 1, 2, 3, 5, 8, 13, 21

def fibonaci(n):
    if n == 0:
        return 0
    elif n == 1:
        return 1
    else:
        return fibonaci(n - 1) + fibonaci(n - 2)
    
print(fibonaci(7))

